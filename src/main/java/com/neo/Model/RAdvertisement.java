public class RAdvertisement {

    private Float city, state, country, international;

    public RAdvertisement() {
    }

    public Float getCity() {
        return city;
    }

    public void setCity(Float city) {
        this.city = city;
    }

    public Float getState() {
        return state;
    }

    public void setState(Float state) {
        this.state = state;
    }

    public Float getCountry() {
        return country;
    }

    public void setCountry(Float country) {
        this.country = country;
    }

    public Float getInternational() {
        return international;
    }

    public void setInternational(Float international) {
        this.international = international;
    }
}
